"""인공신경망이란 실습 파일"""
# 설명: 필요한 도구를 가져와요.
from __future__ import annotations

# 설명: 필요한 도구를 가져와요.
import numpy as np


# 설명: 값을 저장하거나 바꿔요.
LESSON_10MIN = "뉴런은 입력에 가중치를 곱해 더한 뒤 출력값을 만든다."
# 설명: 값을 저장하거나 바꿔요.
PRACTICE_30MIN = "numpy로 1층 순전파를 계산하고 출력 형태를 확인한다."


# 설명: `run` 함수를 만들어요.
def run() -> dict:
    # 3개 샘플, 각 샘플은 4개 특성
    # 설명: 값을 저장하거나 바꿔요.
    X = np.array(
        # 설명: 다음 코드를 실행해요.
        [
            # 설명: 다음 코드를 실행해요.
            [0.2, 0.7, 1.0, -0.3],
            # 설명: 다음 코드를 실행해요.
            [1.2, -0.4, 0.3, 0.8],
            # 설명: 다음 코드를 실행해요.
            [0.0, 0.5, -0.6, 1.1],
        # 설명: 다음 코드를 실행해요.
        ],
        # 설명: 값을 저장하거나 바꿔요.
        dtype=float,
    # 설명: 다음 코드를 실행해요.
    )

    # 1층(입력 4 -> 출력 3)
    # 설명: 값을 저장하거나 바꿔요.
    W = np.array(
        # 설명: 다음 코드를 실행해요.
        [
            # 설명: 다음 코드를 실행해요.
            [0.4, -0.2, 0.1],
            # 설명: 다음 코드를 실행해요.
            [0.7, 0.3, -0.5],
            # 설명: 다음 코드를 실행해요.
            [-0.6, 0.8, 0.2],
            # 설명: 다음 코드를 실행해요.
            [0.2, -0.1, 0.9],
        # 설명: 다음 코드를 실행해요.
        ],
        # 설명: 값을 저장하거나 바꿔요.
        dtype=float,
    # 설명: 다음 코드를 실행해요.
    )
    # 설명: 값을 저장하거나 바꿔요.
    b = np.array([0.1, -0.2, 0.05], dtype=float)

    # 설명: 값을 저장하거나 바꿔요.
    logits = X @ W + b
    # 설명: 값을 저장하거나 바꿔요.
    relu_out = np.maximum(0.0, logits)

    # 설명: 함수 결과를 돌려줘요.
    return {
        # 설명: 다음 코드를 실행해요.
        "chapter": "chapter67",
        # 설명: 다음 코드를 실행해요.
        "topic": "인공신경망이란",
        # 설명: 다음 코드를 실행해요.
        "lesson_10min": LESSON_10MIN,
        # 설명: 다음 코드를 실행해요.
        "practice_30min": PRACTICE_30MIN,
        # 설명: 다음 코드를 실행해요.
        "input_shape": list(X.shape),
        # 설명: 다음 코드를 실행해요.
        "weight_shape": list(W.shape),
        # 설명: 다음 코드를 실행해요.
        "output_shape": list(logits.shape),
        # 설명: 다음 코드를 실행해요.
        "logits": logits.round(4).tolist(),
        # 설명: 다음 코드를 실행해요.
        "relu_output": relu_out.round(4).tolist(),
    # 설명: 다음 코드를 실행해요.
    }


# 설명: 조건이 맞는지 확인해요.
if __name__ == "__main__":
    # 설명: 다음 코드를 실행해요.
    print(run())
